DATA SEGMENT
DATA ENDS
CODE SEGMENT
START:
    MOV AX, DATA
    MOV DS, AX
    MOV AH, 00H
    MOV AL, 13H
    INT 10H
    MOV AX, 0A000H
    MOV ES, AX
    MOV DI, 32160
    MOV AL, 4
    MOV ES:[DI], AL
