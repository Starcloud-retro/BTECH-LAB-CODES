CODE SEGMENT
START:
    MOV AX, DATA
    MOV DS, AX

GAME_LOOP_T1:
    MOV AH, 00H
    MOV AL, 13H
    INT 10H
    MOV AX, 0A000H
    MOV ES, AX

    CALL CLEAR_SCREEN_T1
    CALL DRAW_FLOOR_CEILING_T1
    CALL RENDER_TEXTURED_T1

    MOV AH, 01H
    INT 16H
    JZ GAME_LOOP_T1

    MOV AH, 00H
    INT 16H

    CMP AH, 01H
    JE EXIT_T1
    CMP AH, 4DH
    JE TR_T1
    CMP AH, 4BH
    JE TL_T1
    CMP AH, 48H
    JE MF_T1
    CMP AH, 50H
    JE MB_T1
    JMP GAME_LOOP_T1

TR_T1:
    MOV AX, [PLAYER_ANGLE]
    ADD AX, 5
    CMP AX, 180
    JL TRT1_OK
    SUB AX, 180
TRT1_OK:
    MOV [PLAYER_ANGLE], AX
    JMP GAME_LOOP_T1

TL_T1:
    MOV AX, [PLAYER_ANGLE]
    SUB AX, 5
    CMP AX, 0
    JGE TLT1_OK
    ADD AX, 180
TLT1_OK:
    MOV [PLAYER_ANGLE], AX
    JMP GAME_LOOP_T1

MF_T1:
    MOV AX, [PLAYER_ANGLE]
    MOV SI, AX
    SHL SI, 1
    MOV AX, [COS_TABLE + SI]
    IMUL [MOVE_SPEED]
    SAR AX, 7
    ADD [PLAYER_X], AX
    MOV AX, [PLAYER_ANGLE]
    MOV SI, AX
    SHL SI, 1
    MOV AX, [SIN_TABLE + SI]
    IMUL [MOVE_SPEED]
    SAR AX, 7
    ADD [PLAYER_Y], AX
    JMP GAME_LOOP_T1

MB_T1:
    MOV AX, [PLAYER_ANGLE]
    MOV SI, AX
    SHL SI, 1
    MOV AX, [COS_TABLE + SI]
    IMUL [MOVE_SPEED]
    SAR AX, 7
    SUB [PLAYER_X], AX
    MOV AX, [PLAYER_ANGLE]
    MOV SI, AX
    SHL SI, 1
    MOV AX, [SIN_TABLE + SI]
    IMUL [MOVE_SPEED]
    SAR AX, 7
    SUB [PLAYER_Y], AX
    JMP GAME_LOOP_T1

EXIT_T1:
    MOV AH, 00H
    MOV AL, 03H
    INT 10H
    HLT

CLEAR_SCREEN_T1 PROC
    XOR DI, DI
    MOV CX, 32000
    XOR AX, AX
    REP STOSW
    RET
CLEAR_SCREEN_T1 ENDP

DRAW_FLOOR_CEILING_T1 PROC
    MOV DI, 32000
    MOV CX, 32000
    MOV AX, 0606H
    REP STOSW
    XOR DI, DI
    MOV CX, 32000
    MOV AX, 0101H
    REP STOSW
    RET
DRAW_FLOOR_CEILING_T1 ENDP

RENDER_TEXTURED_T1 PROC
    MOV BX, 0
    MOV CX, 80
RT1_LP:
    PUSH CX
    PUSH BX
    MOV AX, [PLAYER_ANGLE]
    SUB AX, 30
    ADD AX, BX
    CMP AX, 0
    JGE RT1_CHK
    ADD AX, 180
RT1_CHK:
    CMP AX, 180
    JL RT1_OK
    SUB AX, 180
RT1_OK:
    MOV SI, AX
    SHL SI, 1
    MOV DX, [COS_TABLE + SI]
    MOV DI, [SIN_TABLE + SI]
    CALL CAST_RAY_T1
    POP BX
    SHL BX, 2
    CALL DRAW_TEXTURED_WALL
    POP CX
    INC BX
    LOOP RT1_LP
    RET
RENDER_TEXTURED_T1 ENDP

CAST_RAY_T1 PROC
    PUSH BX
    MOV BX, [PLAYER_X]
    MOV SI, [PLAYER_Y]
    MOV CX, 200
CRT1_LP:
    ADD BX, DX
    ADD SI, DI
    PUSH BX
    PUSH SI
    MOV AX, BX
    SHR AX, 8
    MOV BP, AX
    MOV AX, SI
    SHR AX, 8
    PUSH BX
    MOV BX, 10
    MUL BX
    ADD AX, BP
    MOV BP, AX
    POP BX
    CMP BP, 100
    JGE CRT1_HIT
    MOV AL, [MAP + BP]
    POP SI
    POP BX
    CMP AL, 1
    JE CRT1_HIT
    LOOP CRT1_LP
CRT1_HIT:
    POP BX
    MOV [TEMP_DIST_T1], CX
    MOV [WALL_X_T1], BX
    RET
CAST_RAY_T1 ENDP

DRAW_TEXTURED_WALL PROC
    MOV AX, 5000
    MOV CX, [TEMP_DIST_T1]
    CMP CX, 1
    JLE DTW_MAX
    XOR DX, DX
    DIV CX
    CMP AX, 200
    JLE DTW_OK
DTW_MAX:
    MOV AX, 200
DTW_OK:
    ; Get texture column
    MOV SI, [WALL_X_T1]
    AND SI, 7

    MOV CX, AX
    MOV AX, 100
    SUB AX, CX
    SHR AX, 1
    MOV DI, AX

DTW_LP:
    PUSH CX
    PUSH DI

    ; Sample texture
    MOV AX, DI
    AND AX, 7
    MOV DX, 8
    MUL DX
    ADD AX, SI
    MOV SI, AX
    MOV AL, [STONE_TEX + SI]

    ; Draw pixel
    POP SI
    PUSH AX
    MOV AX, SI
    MOV DI, 320
    MUL DI
    ADD AX, BX
    MOV DI, AX
    POP AX
    MOV ES:[DI], AL

    INC SI
    POP CX
    LOOP DTW_LP
    RET
DRAW_TEXTURED_WALL ENDP

TEMP_DIST_T1 DW 0
WALL_X_T1 DW 0

CODE ENDS
END START
