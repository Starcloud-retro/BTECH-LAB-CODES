ASSUME CS:CODE, DS:DATA
DATA SEGMENT
   NUM DW 00005H
   FACT DW ?
DATA ENDS
CODE SEGMENT
      MOV AX,DATA
      MOV DS,AX
      MOV CX,NUM
      MOV DX,0000H
      MOV AX,0001H
   NEXT: MUL CX
         DEC CX
         CMP CX,0001H
         JNZ NEXT

         MOV FACT,AX
         INT 21H
CODE ENDS
END
