; SPRITE1.ASM - 8x8 smiley sprite
ASSUME CS:CODE, DS:DATA
DATA SEGMENT
    SMILEY DB 0,0,15,15,15,15,0,0
           DB 0,15,0,0,0,0,15,0
           DB 15,0,4,0,0,4,0,15
           DB 15,0,0,0,0,0,0,15
           DB 15,0,4,4,4,4,0,15
           DB 15,0,0,0,0,0,0,15
           DB 0,15,0,0,0,0,15,0
           DB 0,0,15,15,15,15,0,0
    SPRITE_X DW 156
    SPRITE_Y DW 96
DATA ENDS
CODE SEGMENT
START:
    MOV AX, DATA
    MOV DS, AX
    MOV AH, 00H
    MOV AL, 13H
    INT 10H
    MOV AX, 0A000H
    MOV ES, AX
    MOV AX, [SPRITE_Y]
    MOV BX, 320
    MUL BX
    ADD AX, [SPRITE_X]
    MOV DI, AX
    LEA SI, SMILEY
    MOV BX, 8
DRAW_SPRITE_ROW:
    PUSH BX
    PUSH DI
    MOV CX, 8
DRAW_SPRITE_COL:
    LODSB
    CMP AL, 0
    JE SKIP_PIXEL
    MOV ES:[DI], AL
SKIP_PIXEL:
    INC DI
    LOOP DRAW_SPRITE_COL
    POP DI
    ADD DI, 320
    POP BX
    DEC BX
    JNZ DRAW_SPRITE_ROW
    MOV AH, 00H
    INT 16H
    MOV AH, 00H
    MOV AL, 03H
    INT 10H
    
    MOV AH, 4CH
    INT 21H
CODE ENDS
END START
