CODE SEGMENT
START:
    MOV AX, DATA
    MOV DS, AX

GAME_LOOP_T2:
    MOV AH, 00H
    MOV AL, 13H
    INT 10H
    MOV AX, 0A000H
    MOV ES, AX

    CALL CLEAR_SCREEN_T2
    CALL RENDER_MULTI_TEX

    MOV AH, 01H
    INT 16H
    JZ GAME_LOOP_T2

    MOV AH, 00H
    INT 16H

    CMP AH, 01H
    JE EXIT_T2
    CMP AH, 4DH
    JE TR_T2
    CMP AH, 4BH
    JE TL_T2
    CMP AH, 48H
    JE MF_T2
    CMP AH, 50H
    JE MB_T2
    JMP GAME_LOOP_T2

TR_T2:
    MOV AX, [PLAYER_ANGLE]
    ADD AX, 5
    CMP AX, 180
    JL TRT2_OK
    SUB AX, 180
TRT2_OK:
    MOV [PLAYER_ANGLE], AX
    JMP GAME_LOOP_T2

TL_T2:
    MOV AX, [PLAYER_ANGLE]
    SUB AX, 5
    CMP AX, 0
    JGE TLT2_OK
    ADD AX, 180
TLT2_OK:
    MOV [PLAYER_ANGLE], AX
    JMP GAME_LOOP_T2

MF_T2:
    MOV AX, [PLAYER_ANGLE]
    MOV SI, AX
    SHL SI, 1
    MOV AX, [COS_TABLE + SI]
    IMUL [MOVE_SPEED]
    SAR AX, 7
    ADD [PLAYER_X], AX
    MOV AX, [PLAYER_ANGLE]
    MOV SI, AX
    SHL SI, 1
    MOV AX, [SIN_TABLE + SI]
    IMUL [MOVE_SPEED]
    SAR AX, 7
    ADD [PLAYER_Y], AX
    JMP GAME_LOOP_T2

MB_T2:
    MOV AX, [PLAYER_ANGLE]
    MOV SI, AX
    SHL SI, 1
    MOV AX, [COS_TABLE + SI]
    IMUL [MOVE_SPEED]
    SAR AX, 7
    SUB [PLAYER_X], AX
    MOV AX, [PLAYER_ANGLE]
    MOV SI, AX
    SHL SI, 1
    MOV AX, [SIN_TABLE + SI]
    IMUL [MOVE_SPEED]
    SAR AX, 7
    SUB [PLAYER_Y], AX
    JMP GAME_LOOP_T2

EXIT_T2:
    MOV AH, 00H
    MOV AL, 03H
    INT 10H
    
    MOV AH, 4CH
    INT 21H

CLEAR_SCREEN_T2 PROC
    XOR DI, DI
    MOV CX, 32000
    XOR AX, AX
    REP STOSW
    RET
CLEAR_SCREEN_T2 ENDP

RENDER_MULTI_TEX PROC
    MOV BX, 0
    MOV CX, 80
RMT_LP:
    PUSH CX
    PUSH BX
    MOV AX, [PLAYER_ANGLE]
    SUB AX, 30
    ADD AX, BX
    CMP AX, 0
    JGE RMT_CHK
    ADD AX, 180
RMT_CHK:
    CMP AX, 180
    JL RMT_OK
    SUB AX, 180
RMT_OK:
    MOV SI, AX
    SHL SI, 1
    MOV DX, [COS_TABLE + SI]
    MOV DI, [SIN_TABLE + SI]
    CALL CAST_RAY_TYPED
    POP BX
    SHL BX, 2
    CALL DRAW_TYPED_WALL
    POP CX
    INC BX
    LOOP RMT_LP
    RET
RENDER_MULTI_TEX ENDP

CAST_RAY_TYPED PROC
    PUSH BX
    MOV BX, [PLAYER_X]
    MOV SI, [PLAYER_Y]
    MOV CX, 200
CRTY_LP:
    ADD BX, DX
    ADD SI, DI
    PUSH BX
    PUSH SI
    MOV AX, BX
    SHR AX, 8
    MOV BP, AX
    MOV AX, SI
    SHR AX, 8
    PUSH BX
    MOV BX, 10
    MUL BX
    ADD AX, BP
    MOV BP, AX
    POP BX
    CMP BP, 100
    JGE CRTY_HIT
    MOV AL, [MAP + BP]
    CMP AL, 0
    JE CRTY_CONT
    MOV [CURRENT_WALL_TYPE], AL
    POP SI
    POP BX
    JMP CRTY_HIT
CRTY_CONT:
    POP SI
    POP BX
    LOOP CRTY_LP
CRTY_HIT:
    POP BX
    MOV [TEMP_DIST_T2], CX
    MOV [WALL_X_T2], BX
    RET
CAST_RAY_TYPED ENDP

DRAW_TYPED_WALL PROC
    MOV AX, 5000
    MOV CX, [TEMP_DIST_T2]
    CMP CX, 1
    JLE DTY_MAX
    XOR DX, DX
    DIV CX
    CMP AX, 200
    JLE DTY_OK
DTY_MAX:
    MOV AX, 200
DTY_OK:
    ; Choose texture based on wall type
    LEA SI, STONE_TEX
    MOV AL, [CURRENT_WALL_TYPE]
    CMP AL, 2
    JNE CHK_BRICK
    LEA SI, WOOD_TEX
    JMP TEX_CHOSEN
CHK_BRICK:
    CMP AL, 3
    JNE TEX_CHOSEN
    LEA SI, BRICK_TEX
TEX_CHOSEN:

    MOV CX, AX
    MOV AX, 100
    SUB AX, CX
    SHR AX, 1
    MOV DI, AX

    MOV AX, [WALL_X_T2]
    AND AX, 7
    MOV [TEX_COL], AX

DTY_LP:
    PUSH CX
    PUSH DI

    MOV AX, DI
    AND AX, 7
    MOV DX, 8
    MUL DX
    ADD AX, [TEX_COL]
    ADD SI, AX
    MOV AL, [SI]

    POP SI
    PUSH AX
    MOV AX, SI
    MOV DI, 320
    MUL DI
    ADD AX, BX
    MOV DI, AX
    POP AX
    MOV ES:[DI], AL

    INC SI
    POP CX
    LOOP DTY_LP
    RET
DRAW_TYPED_WALL ENDP

TEMP_DIST_T2 DW 0
WALL_X_T2 DW 0
TEX_COL DW 0

CODE ENDS
END START
