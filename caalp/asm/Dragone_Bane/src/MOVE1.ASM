CODE SEGMENT
START:
    MOV AX, DATA
    MOV DS, AX

GAME_LOOP_M:
    MOV AH, 00H
    MOV AL, 13H
    INT 10H
    MOV AX, 0A000H
    MOV ES, AX

    CALL CLEAR_SCREEN_M
    CALL RENDER_VIEW_M

    MOV AH, 01H
    INT 16H
    JZ GAME_LOOP_M

    MOV AH, 00H
    INT 16H

    CMP AH, 01H
    JE EXIT_M
    CMP AH, 4DH
    JE TURN_RIGHT_M
    CMP AH, 4BH
    JE TURN_LEFT_M
    CMP AH, 48H
    JE MOVE_FORWARD
    CMP AH, 50H
    JE MOVE_BACKWARD
    JMP GAME_LOOP_M

TURN_RIGHT_M:
    MOV AX, [PLAYER_ANGLE]
    ADD AX, 5
    CMP AX, 360
    JL TR_OK
    SUB AX, 360
TR_OK:
    MOV [PLAYER_ANGLE], AX
    JMP GAME_LOOP_M

TURN_LEFT_M:
    MOV AX, [PLAYER_ANGLE]
    SUB AX, 5
    CMP AX, 0
    JGE TL_OK
    ADD AX, 360
TL_OK:
    MOV [PLAYER_ANGLE], AX
    JMP GAME_LOOP_M

MOVE_FORWARD:
    MOV AX, [PLAYER_ANGLE]
    MOV SI, AX
    SHL SI, 1
    MOV AX, [COS_TABLE + SI]
    MOV BX, [SIN_TABLE + SI]
    ; Scale by speed
    IMUL [MOVE_SPEED]
    SAR AX, 8
    ADD [PLAYER_X], AX
    MOV AX, BX
    IMUL [MOVE_SPEED]
    SAR AX, 8
    ADD [PLAYER_Y], AX
    JMP GAME_LOOP_M

MOVE_BACKWARD:
    MOV AX, [PLAYER_ANGLE]
    MOV SI, AX
    SHL SI, 1
    MOV AX, [COS_TABLE + SI]
    MOV BX, [SIN_TABLE + SI]
    IMUL [MOVE_SPEED]
    SAR AX, 8
    SUB [PLAYER_X], AX
    MOV AX, BX
    IMUL [MOVE_SPEED]
    SAR AX, 8
    SUB [PLAYER_Y], AX
    JMP GAME_LOOP_M

EXIT_M:
    MOV AH, 00H
    MOV AL, 03H
    INT 10H
    
    MOV AH, 4CH
    INT 21H

CLEAR_SCREEN_M PROC
    XOR DI, DI
    MOV CX, 32000
    XOR AX, AX
    REP STOSW
    RET
CLEAR_SCREEN_M ENDP

RENDER_VIEW_M PROC
    ; Same as ROTATE1 render
    MOV BX, 0
    MOV CX, 80
RV_LOOP:
    PUSH CX
    PUSH BX
    MOV AX, [PLAYER_ANGLE]
    SUB AX, 30
    ADD AX, BX
    CMP AX, 0
    JGE RV_CHECK
    ADD AX, 360
RV_CHECK:
    CMP AX, 360
    JL RV_VALID
    SUB AX, 360
RV_VALID:
    MOV SI, AX
    SHL SI, 1
    MOV DX, [COS_TABLE + SI]
    MOV DI, [SIN_TABLE + SI]
    CALL CAST_RAY_M
    POP BX
    SHL BX, 2
    CALL DRAW_WALL_M
    POP CX
    INC BX
    LOOP RV_LOOP
    RET
RENDER_VIEW_M ENDP

CAST_RAY_M PROC
    PUSH BX
    MOV BX, [PLAYER_X]
    MOV SI, [PLAYER_Y]
    MOV CX, 200
CRM_LOOP:
    ADD BX, DX
    ADD SI, DI
    PUSH BX
    PUSH SI
    PUSH AX
    MOV AX, BX
    SHR AX, 8
    MOV BP, AX
    MOV AX, SI
    SHR AX, 8
    PUSH BX
    MOV BX, 10
    MUL BX
    ADD AX, BP
    MOV BP, AX
    POP BX
    CMP BP, 100
    JGE CRM_HIT
    MOV AL, [MAP + BP]
    POP AX
    POP SI
    POP BX
    CMP AL, 1
    JE CRM_HIT
    LOOP CRM_LOOP
CRM_HIT:
    POP BX
    MOV [TEMP_DIST_M], CX
    RET
CAST_RAY_M ENDP

DRAW_WALL_M PROC
    MOV AX, 5000
    MOV CX, [TEMP_DIST_M]
    CMP CX, 1
    JLE DWM_MAX
    XOR DX, DX
    DIV CX
    CMP AX, 200
    JLE DWM_OK
DWM_MAX:
    MOV AX, 200
DWM_OK:
    MOV CX, AX
    MOV AX, 100
    SUB AX, CX
    SHR AX, 1
    MOV SI, AX
DWM_LOOP:
    PUSH CX
    MOV AX, SI
    MOV DI, 320
    MUL DI
    ADD AX, BX
    MOV DI, AX
    MOV AL, 7
    MOV ES:[DI], AL
    INC SI
    POP CX
    LOOP DWM_LOOP
    RET
DRAW_WALL_M ENDP

