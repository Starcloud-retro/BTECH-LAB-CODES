ASSUME CS:CODE,DS:DATA
DATA SEGMENT
   NUM DW 0036H;NUM IS VARIABLE STORING 2BYTE LOCATION
   STR1 DB "THE GIVEN NUMBER IS ODD NUMBER$";VARIABLE FOR PRINTING ODD CONDITION 
   STR2 DB "THE GIVEN NUMBER IS EVEN NUMBER$";VARIABLE FOR PRINTING EVEN STORING EACH WITH 8BIT STORAGE
DATA ENDS
CODE SEGMENT
      MOV AX,DATA
      MOV DS,AX
      MOV AX,NUM
      MOV BL,02H
      DIV BL
      CMP AH,00H
      JNE GO
      LEA DX,STR2
      MOV AH,09H
      INT 21H
      JMP NEXT
   GO: LEA DX,STR1
       MOV AH,09H
       INT 21H
   NEXT: HLT
CODE ENDS
END



